; ***************************************************************************************************************
;
;												Get a keystroke into D.
;
; ***************************************************************************************************************

XIOGetKey:
	sex 	r2 															; fix up after Mark.
	inp 	1 															; read keyboard port
	bz 		XIOGetKey 													; no key, keep trying
	inc 	r2 															; standard return.
	return

